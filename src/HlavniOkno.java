    private void jButtonVysledekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVysledekActionPerformed
        
        String vysl;
        
     if(operace != "")
     {  
        DruheCislo = Double.parseDouble(jTextFieldPromenne.getText());
        if(operace == "+")
        {
            vysledek = PrvniCislo + DruheCislo;
           // vysl = String.format("%.0f",vysledek);
            vysl = String.valueOf(vysledek);
            jTextFieldPromenne.setText(vysl);
        } 
        else if(operace == "-")
        {
            vysledek = PrvniCislo - DruheCislo;
            //vysl = String.format("%.0f",vysledek);
            vysl = String.valueOf(vysledek);
            jTextFieldPromenne.setText(vysl);
        }
        else if(operace == "*")
        {
            vysledek = PrvniCislo * DruheCislo;
            //vysl = String.format("%.0f",vysledek);
            vysl = String.valueOf(vysledek);
            jTextFieldPromenne.setText(vysl);
        }
        else if(operace == "/"){
            if(DruheCislo == 0)
             {
              JOptionPane.showMessageDialog(this, "Nulou dƒõlit nelze!","Chyba", JOptionPane.ERROR_MESSAGE);  
              jTextFieldPromenne.setText("");
             } 
            else
             {
              vysledek = PrvniCislo / DruheCislo;
             //vysl = String.format("%.0f",vysledek);
              vysl = String.valueOf(vysledek);
              jTextFieldPromenne.setText(vysl);
             }
            }
     }
      else 
     {  
         try {
         double vzorec = Float.parseFloat(jTextFieldPromenne.getText());
        // vysl =String.format("%.0f", vzorec);
         vysl = String.valueOf(vysledek);
         jTextFieldPromenne.setText(vysl);
         } catch(Exception e) {
             JOptionPane.showMessageDialog(this,"Toto mi je≈°tƒõ nefunguje :(");
             jTextFieldPromenne.setText("");
         }
     }
    }//GEN-LAST:event_jButtonVysledekActionPerformed

